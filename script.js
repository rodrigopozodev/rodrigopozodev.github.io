function updateMuscleGroups() {
    const bodyPart = document.getElementById('bodyPart').value;
    const muscleGroupSelect = document.getElementById('muscleGroup');
    muscleGroupSelect.innerHTML = '<option value="">Selecciona un grupo muscular...</option>';
    
    // Limpiar resultados al cambiar de parte del cuerpo
    const resultsContainer = document.getElementById('results');
    resultsContainer.innerHTML = '';

    if (bodyPart && data[bodyPart]) {
        const muscleGroups = Object.keys(data[bodyPart]);
        muscleGroups.forEach(group => {
            const option = document.createElement('option');
            option.value = group;
            option.textContent = group;
            muscleGroupSelect.appendChild(option);
        });
    }
}

function showMuscles() {
    const bodyPart = document.getElementById('bodyPart').value;
    const muscleGroup = document.getElementById('muscleGroup').value;
    const resultsContainer = document.getElementById('results');
    resultsContainer.innerHTML = '';

    if (bodyPart && muscleGroup && data[bodyPart] && data[bodyPart][muscleGroup]) {
        const muscles = data[bodyPart][muscleGroup];
        for (const muscle in muscles) {
            const exercises = muscles[muscle];
            const muscleDiv = document.createElement('div');
            muscleDiv.classList.add('muscle');
            muscleDiv.innerHTML = `<h3>${muscle}</h3>`;

            exercises.forEach(exercise => {
                const exerciseDiv = document.createElement('div');
                exerciseDiv.classList.add('exercise');
                exerciseDiv.innerHTML = `
                    <h4>${exercise.ejercicio}</h4>
                    <a href="${exercise.video}" target="_blank">Ver video en YouTube</a>
                `;
                muscleDiv.appendChild(exerciseDiv);
            });

            resultsContainer.appendChild(muscleDiv);
        }
    }
}
